package kyle.leis.eo.operation.predictwaybill.mabang;

public class ApiLinkEntity {
	private String api;
	private String apiAccountId;
	private String apiKey;
	private String mainUrl;
	private String jsonParams;
	public String getApi() {
		return api;
	}
	public void setApi(String api) {
		this.api = api;
	}
	public String getApiAccountId() {
		return apiAccountId;
	}
	public void setApiAccountId(String apiAccountId) {
		this.apiAccountId = apiAccountId;
	}
	public String getApiKey() {
		return apiKey;
	}
	public void setApiKey(String apiKey) {
		this.apiKey = apiKey;
	}
	public String getMainUrl() {
		return mainUrl;
	}
	public void setMainUrl(String mainUrl) {
		this.mainUrl = mainUrl;
	}
	public String getJsonParams() {
		return jsonParams;
	}
	public void setJsonParams(String jsonParams) {
		this.jsonParams = jsonParams;
	}
}
